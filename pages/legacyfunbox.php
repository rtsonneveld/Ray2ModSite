<h1>Rayman 2 Mods</h1>
<h2>Rayman 2 Fun Box by Robin</h2>
<p>Pack of a few fun mods for Rayman 2 on PC using memory editing:</p>
<ul>
<li>Most importantly: the First Person Mod!</li>
<li>A mod that forces your HP to always be at zero, meaning no hits can be taken without a game over.</li>
<li>A mod that messes up the models of objects in the game... use at your own risk!</li>
</ul>
<h3>How to use</h3>
There's three checkboxes to activate any of the three mods.
<ul>
<li>The FPS mod should only be activated when Rayman is in his normal state, standing on the ground.</li>
<li>The other mods can be activated at any time.</li>
<li>Make sure the game is running when activating a mod!</li>
</ul>
