<h1>Home</h1>
<p>Ray2Mod is a modding API written in unmanaged C# which provides the ability to hook into Rayman 2 using <a href="https://easyhook.github.io/">EasyHook</a>, which allows easy access to internal game functions and memory.</p>
<p>On this site you'll find more information on how to develop, and most importantly, play mods using the <a href="?page=modmanager">Mod Manager</a>.</p>
